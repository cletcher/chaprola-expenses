// SUMMARY — Weekly expense summary for the caller
// Multi-user: scoped by PARAM.user_id. Required parameter: PARAM.user_id.
// Totals formatted via PUT D 2 (which emits "$N,NNN.NN") to avoid
// IEEE 754 accumulation artefacts.

OPEN PRIMARY "ledger"

QUERY ledger INTO approved_set WHERE user_id EQ PARAM.user_id AND state EQ "approved"
QUERY ledger INTO pending_set  WHERE user_id EQ PARAM.user_id AND state EQ "pending"

LET approved_total = 0
LET pending_total = 0
LET approved_count = 0
LET pending_count = 0
LET rec = 1

100 READ approved_set rec
    IF EOF GOTO 200
    GET R10 FROM approved_set.amount
    LET approved_total = approved_total + R10
    LET rec = rec + 1
    GOTO 100

200 LET rec = 1
300 READ pending_set rec
    IF EOF GOTO 400
    GET R10 FROM pending_set.amount
    LET pending_total = pending_total + R10
    LET rec = rec + 1
    GOTO 300

400 LET approved_count = approved_set.RECORDCOUNT
    LET pending_count = pending_set.RECORDCOUNT

    PRINT "WEEKLY EXPENSE SUMMARY"
    PRINT "======================"

    MOVE BLANKS U.1 80
    MOVE "Approved: " U.1 10
    PUT approved_count INTO U.11 5 I 0
    MOVE " expenses, " U.16 12
    PUT approved_total INTO U.28 14 D 2
    PRINT 0

    MOVE BLANKS U.1 80
    MOVE "Pending:  " U.1 10
    PUT pending_count INTO U.11 5 I 0
    MOVE " expenses, " U.16 12
    PUT pending_total INTO U.28 14 D 2
    PRINT 0

    PRINT "Generated by chaprola-expenses. Visit the dashboard for details."
