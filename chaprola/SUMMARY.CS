// SUMMARY.CS — Weekly expense summary for the caller
// Multi-user: scoped by PARAM.user_id (from chp_user_ token or passed
// explicitly by anonymous /report callers as 'demo-user').
// Splits the ledger into approved and pending subsets for this user,
// then iterates each to compute dollar totals alongside counts.

QUERY ledger INTO approved_set WHERE user_id EQ PARAM.user_id AND state EQ "approved"
QUERY ledger INTO pending_set  WHERE user_id EQ PARAM.user_id AND state EQ "pending"

LET approved_total = 0
LET rec = 1

100 READ approved_set rec
    IF EOF GOTO 200
    GET R10 FROM approved_set.amount
    LET approved_total = approved_total + R10
    LET rec = rec + 1
    GOTO 100

200 LET pending_total = 0
    LET rec = 1

300 READ pending_set rec
    IF EOF GOTO 400
    GET R10 FROM pending_set.amount
    LET pending_total = pending_total + R10
    LET rec = rec + 1
    GOTO 300

400 PRINT "WEEKLY EXPENSE SUMMARY"
    PRINT "======================"
    PRINT ""
    PRINT "Approved: " + approved_set.RECORDCOUNT + " expenses, $" + approved_total
    PRINT "Pending:  " + pending_set.RECORDCOUNT + " expenses, $" + pending_total
    PRINT ""
    PRINT "Generated by chaprola-expenses. Visit the dashboard for details."
